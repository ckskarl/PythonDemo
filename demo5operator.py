x = 5
y = 2

print('x = 5, y = 2')
# Output: x + y = 7
print('x + y =', x+y)

# Output: x - y = 3
print('x - y =', x-y)

# Output: x * y = 10
print('x * y =', x*y)

# Output: x / y = 2.5
print('x / y =', x/y)

# Output: x // y = 2  5/2=2.5
print('x // y =', x//y)

# Output: x ** y = 25  x^y 5^2
print('x ** y =', x**y)

# x = 5 , y = 2
print(x > y)
print(x != y)

print(x > y and x == 3)  #  &&
print(x > y or x == 3)  # ||
